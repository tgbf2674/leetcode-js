/**
 * Definition for singly-linked list.
 * function ListNode(val, next) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.next = (next===undefined ? null : next)
 * }
 */
/**
 * @param {ListNode} head
 * @param {number} n
 * @return {ListNode}
 */
var removeNthFromEnd = function(head, n) {
  let h1 = head
  let h2 = head
  for (let i = 0;i<n;i++){
    h1 = h1.next
  }
  if(!h1)return head.next
  while(h1.next){
    h1 = h1.next
    h2 = h2.next
  }
  h2.next = h2.next.next
  return head
};
