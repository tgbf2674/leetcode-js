var kthSmallest = function(root, k) {
  // dfs + ä¸­åºéå†
  let res= []
  const inorder = (root)=>{
    if(!root)return root
    inorder(root.left)
    res.push(root.val)
    inorder(root.right)
  }
  inorder(root)
  return res[k-1]
};
